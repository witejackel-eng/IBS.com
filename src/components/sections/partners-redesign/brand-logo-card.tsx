/** Get the brand color for a given partner name. */
function getBrandColor(name: string): string {
  if (SVG_BRANDS[name]) return SVG_BRANDS[name].brandColor;
  if (WORDMARK_STYLES[name]) return WORDMARK_STYLES[name].color;
  return "#1A1A1A";
}

